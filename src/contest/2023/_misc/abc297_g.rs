//#[derive_readable]
struct Problem {
    n: usize,
    l: i64,
    r: i64,
    xs: Vec<i64>,
}

impl Problem {
    fn read() -> Problem {
        input! {
            n: usize,
            l: i64,
            r: i64,
            xs: [i64; n],
        }
        Problem { n, l, r, xs }
    }
    fn grundy(&self, x: i64) -> i64 {
        let l = self.l;
        let r = self.r;
        let modulo = l + r;
        (x % modulo) / l
    }

    fn solve(&self) -> Answer {
        let Problem { n, l, r, xs } = self;
        let ans = xs.iter().copied().map(|x| self.grundy(x)).fold(0, |acc, x| acc ^ x) != 0;
        Answer { ans }
    }
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct Answer {
    ans: bool,
}

impl Answer {
    fn print(&self) {
        // 先手が勝つ場合true
        let msg = if self.ans { "First" } else { "Second" };
        println!("{}", msg);
    }
}

fn main() {
    Problem::read().solve().print();
}

