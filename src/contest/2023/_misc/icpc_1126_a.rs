//#[derive_readable]
struct Problem {
    height: usize,
    width: usize,
    grid: Vec<Vec<u8>>,
}

struct Grid {
    grid: Vec<Vec<u8>>,
    height: usize,
    width: usize,
}

impl Grid {
    fn new(grid: Vec<Vec<u8>>) -> Grid {
        let h = grid.len();
        let w = grid[0].len();
        Grid { grid, height: h, width: w }
    }

    fn is_within(&self, pos: Pos<i64>) -> bool {
        let h = self.height as i64;
        let w = self.width as i64;
        0 <= pos.y && pos.y < h && 0 <= pos.x && pos.x < w
    }

    fn at(&self, pos: Pos<i64>) -> &u8 {
        if self.is_within(pos) {
            &self.grid[pos.y as usize][pos.x as usize]
        } else {
            &b'.'
        }
    }
}

impl Problem {
    fn read() -> Problem {
        input! {
            height: usize,
            width: usize,
            grid: [Bytes; height],
        }
        Problem { height, width, grid }
    }
    fn solve(&self) -> Answer {
        let Problem { height, width, grid } = self;
        let grid = Grid { grid: grid.clone(), height: *height, width: *width };
        let ans = iproduct!(0..*height, 0..*width)
            .map(|(y, x)| {
                std::iter::repeat(pos::DIR4_LIST)
                    .take(7)
                    .multi_cartesian_product()
                    .filter(|dirs| {
                        //
                        let mut next = Pos::new(x as i64, y as i64);
                        let mut str = vec![];
                        str.push(*grid.at(next));
                        for dir in dirs {
                            next += *dir;
                            str.push(*grid.at(next))
                        }
                        str == b"YOKOHAMA"
                    })
                    .count() as i64
            })
            .sum::<i64>();
        Answer { ans }
    }
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct Answer {
    ans: i64,
}

impl Answer {
    fn print(&self) {
        println!("{}", self.ans);
    }
}

fn main() {
    Problem::read().solve().print();
}

