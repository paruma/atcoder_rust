fn main() {
    input! {
        xs: Chars,
    }

    let n = xs.len();
    let ans = (0..=n)
        .tuple_combinations()
        .map(|(begin, end)| {
            if xs[begin] == 't' && xs[end - 1] == 't' && end - begin >= 3 {
                let cnt = xs[begin..end]
                    .iter()
                    .copied()
                    .filter(|ch| *ch == 't')
                    .count();
                let numer = (cnt - 2) as f64;
                let denom = ((end - begin) - 2) as f64;
                numer / denom
            } else {
                0.0
            }
        })
        .max_by(f64::total_cmp)
        .unwrap();

    println!("{}", ans);
}

