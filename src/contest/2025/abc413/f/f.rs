fn main() {
    input! {
        h: usize,
        w: usize,
        k: usize,
        goals: [PosYX1; k],
    }

    let mut tables = vec![vec![i64::MAX; w]; h];

    // priority queue の方が良い？
    let mut que: BinaryHeap<(Reverse<i64>, Pos)> = BinaryHeap::new();

    for &g in &goals {
        tables[g] = 0;
        que.push((Reverse(0), g));
    }

    while let Some((Reverse(score), current)) = que.pop() {
        if tables[current] < score {
            continue;
        }
        for next in Pos::around4_pos_iter(current) {
            if !tables.is_within(next) {
                continue;
            }
            // dbg!(next);

            // ここの処理重そう
            let neighbour_scores = Pos::around4_pos_iter(next)
                .filter(|p| tables.is_within(*p))
                .map(|p| tables[p])
                .sorted()
                .collect_vec();
            let score = if neighbour_scores.len() < 2 {
                i64::MAX
            } else {
                neighbour_scores[1].saturating_add(1)
            };
            if tables[next] > score {
                tables[next] = score;
                que.push((Reverse(score), next));
            }
        }
    }

    let ans: i64 = tables
        .iter()
        .flatten()
        .copied()
        .map(|x| if x == i64::MAX { 0 } else { x })
        .sum::<i64>();
    println!("{}", ans);
}

