#[derive(Clone, Copy, Debug, PartialEq, Eq)]
#[derive_readable]
struct Group {
    arrival_time: i64, // 待ち行列に並ぶ時刻
    time_len: i64,     // 入店してから退店するまで
    n_people: i64,     // 人数
}
fn main() {
    input! {
        n: usize,
        k: i64,
        gs: [Group; n],
    }
    // (time, type, group idx)
    // type 1: 到着して待ち行列に並ぶ
    // type 2: 退店する (同時刻の場合は退店処理を先に呼ぶ)
    let mut pq: BinaryHeap<(Reverse<i64>, i64, usize)> = BinaryHeap::new();
    let mut que: Queue<usize> = Queue::new();
    let mut ans: Vec<i64> = vec![-2; n];

    let mut cnt = 0_i64; // レストランにいる客の人数

    for (i, g) in gs.iter().copied().enumerate() {
        pq.push((Reverse(g.arrival_time), 1, i));
    }

    while let Some((Reverse(time), ty, gidx)) = pq.pop() {
        if ty == 1 {
            // 待ち行列追加
            que.push(gidx);
        } else {
            // 退店
            cnt -= gs[gidx].n_people;
        }

        while let Some(&front_gidx) = que.peek() {
            if cnt + gs[front_gidx].n_people <= k {
                que.pop();
                pq.push((Reverse(time + gs[front_gidx].time_len), 2, front_gidx));
                ans[front_gidx] = time;
                cnt += gs[front_gidx].n_people;
            } else {
                break;
            }
        }
    }

    print_vec(&ans);
}

