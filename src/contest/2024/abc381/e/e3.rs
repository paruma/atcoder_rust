// 1,2,\ の場所を Vec で管理する。Vec でも k 個勧めるのは簡単

// lower_bounds(xs)[i] = xs.lower_bound(i)
fn lower_bounds(xs: &[usize], size: usize) -> Vec<usize> {
    let mut cur = 0;
    let mut ans = vec![usize::MAX; size];
    for i in 0..size {
        ans[i] = cur;
        if xs.get(cur) == Some(&i) {
            cur += 1;
        }
    }
    ans
}
// 集合をソートした Vec で管理する
// #[fastout]
fn main() {
    input! {
        n: usize,
        q: usize,
        s: Chars,
        qs: [(Usize1, Usize1); q],
    }

    let poss1 = s.iter().copied().positions(|ch| ch == '1').collect_vec();
    let poss2 = s.iter().copied().positions(|ch| ch == '2').collect_vec();
    let poss_slash = s.iter().copied().positions(|ch| ch == '/').collect_vec();

    let lb_poss1 = lower_bounds(&poss1, n);
    let lb_poss2 = lower_bounds(&poss2, n);
    let lb_poss_slash = lower_bounds(&poss_slash, n);

    for (l, r) in qs {
        let len = bin_search(-1, n as i64, |len| {
            let len = len as usize;

            if len == 0 {
                let Some(&cur2) = poss_slash.get(lb_poss_slash[l]) else {
                    return false;
                };
                return cur2 <= r;
            }

            let Some(&cur1) = poss1.get(lb_poss1[l] + len - 1) else {
                return false;
            };
            let Some(&cur2) = poss_slash.get(lb_poss_slash[cur1]) else {
                return false;
            };
            let Some(&cur3) = poss2.get(lb_poss2[cur2] + len - 1) else {
                return false;
            };
            cur3 <= r
        });
        let ans = if len == -1 { 0 } else { 2 * len + 1 };
        println!("{}", ans);
    }
}

