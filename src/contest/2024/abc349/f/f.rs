//#[derive_readable]
#[derive(Debug)]
struct Problem {
    n: usize,
    m: i64,
    xs: Vec<i64>,
}

fn multi_lcm(xs: &[i64]) -> Option<i64> {
    let mut ans = 1;
    for x in xs.iter() {
        let gcd = gcd(ans, *x);
        let tmp = (ans / gcd).checked_mul(*x);
        if tmp.is_none() {
            return None;
        }
        ans = tmp.unwrap();
    }
    Some(ans)
}

impl Problem {
    fn read() -> Problem {
        input! {
            n: usize,
            m: i64,
            xs: [i64; n],
        }
        Problem { n, m, xs }
    }
    fn solve(&self) -> Answer {
        // 998
        let ans = 0;
        Answer { ans }
    }

    #[allow(dead_code)]
    fn solve_naive(&self) -> Answer {
        let ans = self
            .xs
            .iter()
            .copied()
            .powerset()
            .filter(|xs_sub| !xs_sub.is_empty())
            .map(|xs_sub| multi_lcm(&xs_sub))
            .filter(|x| *x == Some(self.m))
            .count()
            % 998244353;
        let ans = ans as i64;
        Answer { ans }
    }
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct Answer {
    ans: i64,
}

impl Answer {
    fn print(&self) {
        println!("{}", self.ans);
    }
}

fn main() {
    Problem::read().solve_naive().print();
}

