//#[derive_readable]
struct Problem {
    t: Vec<u8>,
    n: usize,
    ss: Vec<Vec<Vec<u8>>>,
}

#[allow(unused_macros)]
macro_rules! chmin {
    ($a: expr, $b: expr) => {
        if $a > $b {
            $a = $b;
            true
        } else {
            false
        }
    };
}

impl Problem {
    fn read() -> Problem {
        input! {
            t: Bytes,
            n: usize,
        }

        let ss = (0..n)
            .map(|_| {
                input! {
                    m: usize,
                }
                (0..m)
                    .map(|_| {
                        input! {
                            s: Bytes,
                        }
                        s
                    })
                    .collect_vec()
            })
            .collect_vec();

        Problem { t, n, ss }
    }
    fn solve(&self) -> Answer {
        let mut dp = vec![vec![Inf; self.t.len() + 1]; self.n + 1];
        dp[0][0] = Fin(0);
        for bag_i in 0..self.n {
            for str_i in 0..=self.t.len() {
                // 袋iは使わない
                chmin!(dp[bag_i + 1][str_i], dp[bag_i][str_i]);

                // 袋iを使う
                for s in &self.ss[bag_i] {
                    // t[str_i:] の先頭が sと一致する
                    if self.t[str_i..].starts_with(s) {
                        chmin!(dp[bag_i + 1][str_i + s.len()], dp[bag_i][str_i] + Fin(1));
                    }
                }
            }
        }
        // eprintln!("{}", table!(&dp));
        let ans = dp[self.n][self.t.len()].get_fin_or(-1);
        Answer { ans }
    }
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct Answer {
    ans: i64,
}

impl Answer {
    fn print(&self) {
        println!("{}", self.ans);
    }
}

fn main() {
    Problem::read().solve().print();
}

